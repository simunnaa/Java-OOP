package BasicJava;

public class DataType {
    public static void main(String[] args) {
        boolean b = true;
        char c = 'a';
        int a = 123;
        float d = 21;
        double e = 12.22;
        short s = 11;
        System.out.println("Boolean is "+b);
        System.out.println("Char is "+c);
        System.out.println("Int is "+a);
        System.out.println("Float is "+d);
        System.out.println("Double is "+e);
        System.out.println("Short is "+s);

    }
}
